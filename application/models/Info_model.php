    // get data by id
    function get_by_id($id)
    {
        $this->db->where($this->id, $id);
        return $this->db->get($this->table)->row();
    }
    function get_info($users_level){



        $user_id = $this->session->userdata('user_id');
        $kantor_id = $this->db->select('id_kantor')
                ->from('users')
                ->where('id',$user_id)
                ->get()
                ->row();

        $this->db->order_by('tgl','desc');
        $this->db->where('id_kantor',$kantor_id->id_kantor);
        $this->db->group_by('tgl');
        $result = $this->db->get('info')->result();
        $data_info = array();
        foreach ($result as $info) {
            $d_info = $this->get_by_date($info->tgl,$users_level);
            if(!empty($d_info)){
            $data_info[] = array(
                              'tgl'=>$info->tgl,
                              'info'=>$d_info
                            );
            }
        }
        return $data_info;
    }
    function get_by_date($tgl,$users_level){
        $this->db->where('tgl', $tgl);
        $this->db->order_by('id', 'desc');
        $result = $this->db->get('info')->result();
        $data = array();
        foreach ($result as $r) {
            if($r->tujuan == $users_level || $r->tujuan == 0){
                $data[] = $r->info;
            }
        }
        return $data;
    }
    
    // get total rows
    function total_rows($q = NULL) {
        $this->db->like('id', $q);
	$this->db->or_like('tgl', $q);
	$this->db->or_like('info', $q);
	$this->db->from($this->table);
        return $this->db->count_all_results();
    }

    // get data with limit and search
    function get_limit_data($limit, $start = 0, $q = NULL) {
        $this->db->order_by($this->id, $this->order);
        $this->db->like('id', $q);
    	$this->db->or_like('tgl', $q);
    	$this->db->or_like('info', $q);
    	$this->db->limit($limit, $start);
        return $this->db->get($this->table)->result();
    }

