class Info_model extends CI_Model
{

    public $table = 'info';
    public $id = 'id';
    public $order = 'DESC';

    function __construct()
    {
        parent::__construct();
    }

    // datatables
    function json() {


        
        $user_id = $this->session->userdata('user_id');
        $kantor_id = $this->db->select('id_kantor')
                ->from('users')
                ->where('id',$user_id)
                ->get()
                ->row();
        $this->datatables->select('info.id,info.tgl,info.info,pil_level.level as tujuan');
        $this->datatables->from('info');
        //add this line for join
        $this->datatables->join('pil_level', 'info.tujuan = pil_level.id','left');
        $this->datatables->where('info.id_kantor',$kantor_id->id_kantor);
        $this->datatables->add_column('action', anchor(site_url('admin/info/read/$1'),'<button class="btn btn-xs btn-success"><i class="ace-icon fa fa-check bigger-120"></i></button>')."&nbsp;&nbsp;".anchor(site_url('admin/info/update/$1'),'<button class="btn btn-xs btn-info"><i class="ace-icon fa fa-pencil bigger-120"></i></button>')."&nbsp;&nbsp;".anchor(site_url('admin/info/delete/$1'),'<button class="btn btn-xs btn-danger"><i class="ace-icon fa fa-trash-o bigger-120"></i></button>','onclick="javasciprt: return confirm(\'Are You Sure ?\')"'), 'id');
        return $this->datatables->generate();
    }

    // get all
    function get_all()
    {
        $user_id = $this->session->userdata('user_id');
        $kantor_id = $this->db->select('id_kantor')
                ->from('users')
                ->where('id',$user_id)
                ->get()
                ->row();
        $this->db->order_by($this->id, $this->order);
        return $this->db->where('id_kantor', $kantor_id->id_kantor)->get($this->table)->result();
